const sanitizePath = (url) => {
  const rawPath = url.replace(ASSET_PREFIX, "").split("?")[0];
  const decoded = decodeURIComponent(rawPath);
  const cleaned = decoded
    .replace(/[()]/g, "")
    .replace(/\[|\]/g, "")
    .replace(/[^a-zA-Z0-9/_\-\.]/g, "");
  return cleaned;
};

const downloadAssets = async () => {
  for (const url of assetUrls) {
    const fullUrl = `${BASE_URL}${url}`;
    const cleanedPath = sanitizePath(url);
    const outputPath = path.join(OUTPUT_DIR, cleanedPath);

    console.log(`Downloading ${fullUrl} → ${outputPath}`);
    const res = await fetch(fullUrl);
    if (!res.ok) {
      console.error(`Failed to fetch ${fullUrl}: ${res.status}`);
      continue;
    }
    const buffer = await res.buffer();
    await fs.mkdir(path.dirname(outputPath), { recursive: true });
    await fs.writeFile(outputPath, buffer);
  }
};

const updateHtmlReferences = async () => {
  const files = await getAllHtmlFiles(DIST_DIR);
  for (const file of files) {
    const html = await fs.readFile(file, "utf-8");
    const $ = cheerio.load(html);

    $("link[href]").each((_, el) => {
      const $el = $(el);
      const val = $el.attr("href");
      if (val && val.startsWith(ASSET_PREFIX)) {
        const newPath = `/assets/css/${sanitizePath(val)}`;
        $el.attr("href", newPath);
      }
      if ($el.attr("data-precedence")) {
        $el.removeAttr("data-precedence");
      }
    });

    const formatted = await prettier.format($.html(), { parser: "html" });
    await fs.writeFile(file, formatted);
    console.log(`Updated & formatted ${file}`);
  }
};

const getAllHtmlFiles = async (dir) => {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const res = path.resolve(dir, entry.name);
      return entry.isDirectory() ? getAllHtmlFiles(res) : res.endsWith(".html") ? [res] : [];
    })
  );
  return files.flat();
};

const main = async () => {
  await downloadAssets();
  await updateHtmlReferences();
};

main().catch(console.error);
